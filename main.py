def handle_photo(message, project_name):
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –ø–æ–ª—É—á–µ–Ω–Ω–æ–µ —Ñ–æ—Ç–æ –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç –µ–≥–æ.
    
    """
    if message.content_type != 'photo':
        bot.send_message(message.chat.id, '–≠—Ç–æ –Ω–µ —Ñ–æ—Ç–æ. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø—Ä–∏—à–ª–∏ —Ñ–æ—Ç–æ.')
        bot.register_next_step_handler(message, handle_photo, project_name=project_name)
        return
    
    file_id = message.photo[-1].file_id
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ñ–æ—Ç–æ –ª–æ–∫–∞–ª—å–Ω–æ
    file_info = bot.get_file(file_id)
    downloaded_file = bot.download_file(file_info.file_path)
    photo_path = f"photos/{project_name}.jpg"
    
    # –°–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É photos –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
    if not os.path.exists("photos"):
        os.makedirs("photos")
        
    with open(photo_path, 'wb') as new_file:
        new_file.write(downloaded_file)
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—É—Ç—å –∫ —Ñ–æ—Ç–æ –≤ –ë–î
    user_id = message.from_user.id
